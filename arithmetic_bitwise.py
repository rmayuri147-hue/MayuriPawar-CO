# Arithematic and Bitwise operator
x = 10
print("Initial value of x:", x)

# += 
x += 5
print("After x += 5:", x)  # 15

# -= 
x -= 3
print("After x -= 3:", x)  # 12

# *= 
x *= 2
print("After x *= 2:", x)  # 24

# /= 
x /= 4
print("After x /= 4:", x)  # 6.0

# //= 
x //= 2
print("After x //= 2:", x)  # 3.0

# %= 
x %= 2
print("After x %= 2:", x)  # 1.0

# **= 
x **= 3
print("After x **= 3:", x)  # 1.0

# &= 
x = 5
x &= 3
print("After x &= 3:", x)  # 1

# |= 
x |= 2
print("After x |= 2:", x)  # 3

# ^= 
x ^= 1
print("After x ^= 1:", x)  # 2

# <<= 
x <<= 1
print("After x <<= 1:", x)  # 4

# >>= 
x >>= 2
print("After x >>= 2:", x)  # 1
