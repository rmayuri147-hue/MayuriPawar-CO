text = "  Hello, I'm Mayuri Pawar' "

# 1. strip() 
print("1. strip():", text.strip())

# 2. lower() 
print("2. lower():", text.lower())

# 3. upper() 
print("3. upper():", text.upper())

# 4. replace() 
print("4. replace():", text.replace("May", "User"))

# 5. split() 
print("5. split():", text.split())

# 6. find() 
print("6. find():", text.find("Welcome"))

# 7. count()
print("7. count('o'):", text.count("o"))

# 8. startswith()
print("8. startswith('  Hello'):", text.startswith("  Hello"))

# 9. endswith() 
print("9. endswith('.'):", text.endswith("."))

# 10. isalpha() 
sample = "Python"
print("10. isalpha():", sample.isalpha())
